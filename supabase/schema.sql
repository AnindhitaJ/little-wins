-- Little Wins — single-owner Supabase schema
-- All anonymous browser sessions share one private-app profile.
-- Run this entire file in the Supabase SQL Editor after deploying the matching frontend files.

create extension if not exists pgcrypto;

create table if not exists public.settings (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null unique default '00000000-0000-0000-0000-000000000001'::uuid,
  reward_name text not null default 'Blind Pack' check (char_length(reward_name) between 1 and 80),
  target_days integer not null default 7 check (target_days between 1 and 365),
  theme text not null default 'sakura' check (theme in ('sakura', 'strawberry', 'lavender', 'peach', 'bubblegum')),
  daily_reminder_time time without time zone not null default '20:00',
  show_celebration boolean not null default true,
  enable_sound boolean not null default true,
  current_streak integer not null default 0 check (current_streak >= 0),
  best_streak integer not null default 0 check (best_streak >= 0),
  total_checkins integer not null default 0 check (total_checkins >= 0),
  total_no_spend integer not null default 0 check (total_no_spend >= 0),
  total_spend integer not null default 0 check (total_spend >= 0),
  total_rewards integer not null default 0 check (total_rewards >= 0),
  last_checkin_date date,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.daily_logs (
  id bigint generated by default as identity primary key,
  user_id uuid not null default '00000000-0000-0000-0000-000000000001'::uuid,
  log_date date not null,
  status text not null check (status in ('no_spend', 'spent')),
  created_at timestamptz not null default now(),
  unique (user_id, log_date)
);

create table if not exists public.rewards (
  id bigint generated by default as identity primary key,
  user_id uuid not null default '00000000-0000-0000-0000-000000000001'::uuid,
  earned_date date not null,
  reward_name text not null check (char_length(reward_name) between 1 and 80),
  streak_length integer not null check (streak_length > 0),
  claimed boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists public.achievements (
  id bigint generated by default as identity primary key,
  user_id uuid not null default '00000000-0000-0000-0000-000000000001'::uuid,
  achievement_key text not null check (achievement_key in (
    'first_checkin',
    'first_reward',
    'seven_day_streak',
    'thirty_no_spend',
    'ten_rewards',
    'hundred_checkins'
  )),
  unlocked_at timestamptz not null default now(),
  unique (user_id, achievement_key)
);

create table if not exists public.wishlist (
  id bigint generated by default as identity primary key,
  user_id uuid not null default '00000000-0000-0000-0000-000000000001'::uuid,
  name text not null check (char_length(name) between 1 and 120),
  category text check (category is null or char_length(category) <= 60),
  priority smallint not null default 2 check (priority between 1 and 3),
  owned boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists daily_logs_user_date_idx on public.daily_logs (user_id, log_date desc);
create index if not exists daily_logs_user_status_idx on public.daily_logs (user_id, status);
create index if not exists rewards_user_date_idx on public.rewards (user_id, earned_date desc);
create index if not exists achievements_user_unlocked_idx on public.achievements (user_id, unlocked_at desc);
create index if not exists wishlist_user_owned_idx on public.wishlist (user_id, owned, created_at desc);
create index if not exists wishlist_user_name_idx on public.wishlist (user_id, lower(name));

-- Convert an existing per-device anonymous installation into one shared profile.
-- The profile with the most check-ins is preserved; other accidental anonymous
-- profiles are removed. Re-running this file is safe.
alter table public.settings drop constraint if exists settings_user_id_fkey;
alter table public.daily_logs drop constraint if exists daily_logs_user_id_fkey;
alter table public.rewards drop constraint if exists rewards_user_id_fkey;
alter table public.achievements drop constraint if exists achievements_user_id_fkey;
alter table public.wishlist drop constraint if exists wishlist_user_id_fkey;

alter table public.settings alter column user_id set default '00000000-0000-0000-0000-000000000001'::uuid;
alter table public.daily_logs alter column user_id set default '00000000-0000-0000-0000-000000000001'::uuid;
alter table public.rewards alter column user_id set default '00000000-0000-0000-0000-000000000001'::uuid;
alter table public.achievements alter column user_id set default '00000000-0000-0000-0000-000000000001'::uuid;
alter table public.wishlist alter column user_id set default '00000000-0000-0000-0000-000000000001'::uuid;

do $$
declare
  v_owner_id constant uuid := '00000000-0000-0000-0000-000000000001'::uuid;
  v_source_id uuid;
begin
  select user_id
  into v_source_id
  from public.settings
  order by (user_id = v_owner_id) desc, total_checkins desc, updated_at desc
  limit 1;

  if v_source_id is null then
    insert into public.settings (user_id) values (v_owner_id);
  else
    delete from public.daily_logs where user_id <> v_source_id;
    delete from public.rewards where user_id <> v_source_id;
    delete from public.achievements where user_id <> v_source_id;
    delete from public.wishlist where user_id <> v_source_id;
    delete from public.settings where user_id <> v_source_id;

    if v_source_id <> v_owner_id then
      update public.daily_logs set user_id = v_owner_id where user_id = v_source_id;
      update public.rewards set user_id = v_owner_id where user_id = v_source_id;
      update public.achievements set user_id = v_owner_id where user_id = v_source_id;
      update public.wishlist set user_id = v_owner_id where user_id = v_source_id;
      update public.settings set user_id = v_owner_id where user_id = v_source_id;
    end if;
  end if;
end;
$$;

