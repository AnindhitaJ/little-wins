using (user_id = '00000000-0000-0000-0000-000000000001'::uuid);

revoke all on public.settings, public.daily_logs, public.rewards, public.achievements, public.wishlist from anon, authenticated;
grant select on public.settings to authenticated;
grant update (reward_name, target_days, theme, daily_reminder_time, show_celebration, enable_sound) on public.settings to authenticated;
grant select on public.daily_logs, public.rewards, public.achievements to authenticated;
grant select, insert, update, delete on public.wishlist to authenticated;
grant usage, select on all sequences in schema public to authenticated;

revoke all on function public.set_updated_at() from public, anon, authenticated;
revoke all on function public.unlock_achievements(uuid) from public, anon, authenticated;
revoke all on function public.ensure_user_profile() from public, anon, authenticated;
revoke all on function public.check_in(text, date) from public, anon, authenticated;
revoke all on function public.get_statistics(date) from public, anon, authenticated;
revoke all on function public.get_backup_data() from public, anon, authenticated;
revoke all on function public.restore_backup_data(jsonb) from public, anon, authenticated;

grant execute on function public.ensure_user_profile() to authenticated;
grant execute on function public.check_in(text, date) to authenticated;
grant execute on function public.get_statistics(date) to authenticated;
grant execute on function public.get_backup_data() to authenticated;
grant execute on function public.restore_backup_data(jsonb) to authenticated;

-- All authenticated anonymous sessions are intentionally mapped to the same owner UUID.
-- Anyone who can open the deployed app can therefore view and change this shared data.
-- Direct writes to daily_logs, rewards, and achievements remain restricted to the
-- check_in RPC so streak and reward updates stay atomic.
